                            <h3 class=" fw-bold">
                                PROGRESSIVE DISCIPLINE CHECKLIST
                            </h3>

                            <p class="fw-light para">
                                Follow these steps in exercising progressive discipline with an employee:
                            </p>

                            <h5 class="fw-bold">Before the Meeting</h5>
                            <ul class="s-15 fw-light">
                                <li> Arrange to meet with the employee privately. Do not discipline an employee in
                                    public or in front of other workers.</li>
                                <li> Prepare for the meeting by reviewing your notes and files about both the specific
                                    incident or problem in question and any past discipline taken, either verbal or
                                    written.</li>
                            </ul>



                            <h5 class="fw-bold">During the Meeting</h5>
                            <ul class="s-15 fw-light">
                                <li> Explain to the employee why you’ve called the meeting if the employee doesn’t know
                                    already.</li>
                                <li> State the specific problem in terms of actual performance and desired performance.
                                </li>
                                <li> Review your progressive discipline policy/program with the employee and explain
                                    what steps have been taken already and what the next step is.</li>
                                <li> Give the employee a chance to respond, explain and defend his or her actions.</li>
                                <li> Acknowledge the employee’s story and be sure to include it in your notes of the
                                    discipline session.</li>
                                <li> Tell the employee that you expect his or her behavior to change. Give specific
                                    examples and suggestions. </li>
                                <li> Indicate your confidence in the employee’s ability and willingness to change the
                                    behavior.</li>
                                <li> Have the employee repeat back to you or otherwise confirm that he or she
                                    understands the problem and is clear on what changes are expected.</li>
                                <li> Explain to the employee that you will write a memo summarizing the session as
                                    documentation.</li>
                                <li>
                                    Reassure the employee that you value his or her work and that you want to work
                                    with the employee to make sure that he or she can continue to work at your business.
                                </li>
                            </ul>


